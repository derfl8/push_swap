#include "push_swap.h"

void sa(t_tab **a)
{
    t_tab   *tmp;
    t_tab   *tmp2;

    tmp = *a;
    tmp2 = tmp -> next;
    tmp -> next = tmp2 -> next;
    tmp2 -> next = tmp;
    *a = tmp2;
}